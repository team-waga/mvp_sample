export function getMarketplaceItem(id: string): MarketplaceItem | undefined {
  return marketplaceItems.find((item) => item.id === id)
}

export function getRecommendedItems(currentItemId?: string, limit = 3): MarketplaceItem[] {
  // If we have a current item, exclude it from recommendations
  const filteredItems = currentItemId ? marketplaceItems.filter((item) => item.id !== currentItemId) : marketplaceItems

  // Sort by rating and return top items
  return [...filteredItems].sort((a, b) => b.rating - a.rating).slice(0, limit)
}

export function getFeaturedItems(limit = 4): MarketplaceItem[] {
  return marketplaceItems.filter((item) => item.featured).slice(0, limit)
}

export function filterMarketplaceItems(filters: any): MarketplaceItem[] {
  return marketplaceItems.filter((item) => {
    // Search text
    if (
      filters.search &&
      !item.name.toLowerCase().includes(filters.search.toLowerCase()) &&
      !item.description.toLowerCase().includes(filters.search.toLowerCase())
    ) {
      return false
    }

    // Origin
    if (filters.origin && item.origin !== filters.origin) {
      return false
    }

    // Process
    if (filters.process && item.process !== filters.process) {
      return false
    }

    // Roast Profile
    if (filters.roastProfile && item.roastProfile !== filters.roastProfile) {
      return false
    }

    // Price Range
    if (item.price < filters.priceRange[0] || item.price > filters.priceRange[1]) {
      return false
    }

    // Tags
    if (filters.tags.length > 0 && !filters.tags.some((tag: string) => item.tags.includes(tag))) {
      return false
    }

    // Rating
    if (item.rating < filters.minRating) {
      return false
    }

    // Availability
    if (filters.showOnlyAvailable && item.availableQuantity <= 0) {
      return false
    }

    return true
  })
}

